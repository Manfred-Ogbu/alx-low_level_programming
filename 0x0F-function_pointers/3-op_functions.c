#include "3-calc.h"

/**
 * op_add - runs addition calculation on a and b;
 * @a: int value.
 * @b: int value.
 *
 * Return: Result of addition.
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - runs subtraction calculation on a and b;
 * @a: int value.
 * @b: int value.
 *
 * Return: Result of subtraction.
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - runs product calculation on a and b;
 * @a: int value.
 * @b: int value.
 *
 * Return: Result of multipication.
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - runs a division calculation on a and b;
 * @a: int value.
 * @b: int value.
 *
 * Return: Result of division.
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - runs a moldulus calculation on a and b;
 * @a: int value.
 * @b: int value.
 *
 * Return: Result of modulus operation.
 */
int op_mod(int a, int b)
{
	return (a % b);
}

